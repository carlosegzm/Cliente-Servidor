    private void jButtonListarArquivosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListarArquivosActionPerformed
        try {
            FileMessage req = new FileMessage();
            req.cmd = "list_req";

            FileMessage resp = FileController.enviarComando(req);

            // cria o modelo da jlist
            DefaultListModel<String> model = new DefaultListModel<>();

            if (resp.files != null) {
                for (String nome : resp.files) {
                    model.addElement(nome); // add cada nome de arquivo ao modelo
                }
            }

            jListListarArquivos.setModel(model); // aplica o modelo na lista
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao listar arquivos: " + e.getMessage());
        }
    }//GEN-LAST:event_jButtonListarArquivosActionPerformed

