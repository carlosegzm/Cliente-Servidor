    private void jButtonEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEnviarActionPerformed
        try {
            String nome = jTextFieldCaminhoArquivo.getText().trim();
            if (nome.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Informe o nome do arquivo.");
                return;
            }

            String caminho = "cliente/arquivos/" + nome;
            File file = new File(caminho);
            if (!file.exists()) {
                JOptionPane.showMessageDialog(this, "Arquivo n√£o encontrado em: " + caminho);
                return;
            }

            FileMessage req = new FileMessage();
            req.cmd = "put_req";
            req.file = nome; // s√≥ o nome, n√£o o caminho
            req.value = FileUtil.fileToBase64(caminho);
            req.hash = FileUtil.hashFile(caminho);

            FileMessage resp = FileController.enviarComando(req);
            JOptionPane.showMessageDialog(this, "Status: " + resp.status);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao enviar: " + e.getMessage());
        }
    }//GEN-LAST:event_jButtonEnviarActionPerformed

