    private void jButtonBaixarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBaixarActionPerformed
        try {
            String nome = jTextFieldNomeArquivo.getText().trim();
            if (nome.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Informe o nome do arquivo.");
                return;
            }

            FileMessage req = new FileMessage();
            req.cmd = "get_req";
            req.file = nome;

            FileMessage resp = FileController.enviarComando(req);
            if (resp.value == null || resp.value.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Arquivo n√£o encontrado.");
                return;
            }

            File pasta = new File("cliente/arquivos");
            pasta.mkdirs(); // cria pasta se n√£o existir

            String caminho = "cliente/arquivos/" + nome;
            FileUtil.base64ToFile(resp.value, caminho);
            JOptionPane.showMessageDialog(this, "Arquivo salvo em: " + caminho);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao baixar: " + e.getMessage());
        }
    }//GEN-LAST:event_jButtonBaixarActionPerformed

